package MVCJDBC;

public class Student {
	int rollno,marks;
	String name;
	public int getRollno()
	{
		return rollno;		
	}
	public int getMarks() {
		return marks;
	}
	public String getName() {
		return name;
	}
	public void setMarks(int marks) {
		this.marks = marks;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setRollno(int rollno) {
		this.rollno = rollno;
	}
	

}
